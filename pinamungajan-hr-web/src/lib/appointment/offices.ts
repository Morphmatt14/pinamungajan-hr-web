/**
 * Normalize office name from OCR text
 * Fixes common OCR errors and typos
 */
export function normalizeOfficeName(text: string): string {
  return text
    .replace(/OFTHE/gi, "OF THE")
    .replace(/OFFICEOFTHE/gi, "OFFICE OF THE")
    .replace(/MUNCIPAL/gi, "MUNICIPAL")
    .replace(/MUNICIPAL\s+MUNICIPAL/gi, "MUNICIPAL")
    .replace(/SANGGUNIANG\s+BAVAN/gi, "SANGGUNIANG BAYAN")
    .replace(/DEVELORMENT/gi, "DEVELOPMENT")
    .replace(/\s+/g, " ")
    .trim();
}
